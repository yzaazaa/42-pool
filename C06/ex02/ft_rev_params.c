#include <unistd.h>

void	ft_putchar(char c)
{
	write(1, &c, 1);
}

void	ft_putstr(char *str)
{
	while (*str)
	{
		ft_putchar(*str);
		str++;
	}
}

int	main(int ac, char **av)
{
	int	i;

	if (ac > 1)
	{
		i = ac - 1;
		while (i)
		{
			ft_putstr(av[i--]);
			ft_putchar('\n');
		}
	}
	return (0);
}

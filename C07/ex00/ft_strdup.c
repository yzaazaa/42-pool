#include <stdlib.h>

int	ft_strlen(char *src)
{
	int	i;

	i = 0;
	while (src[i])
		i++;
	return (i);
}

char	*ft_strcpy(char *dest, char *src)
{
	int	i;

	i = 0;
	while (src[i])
	{
		dest[i] = src[i];
		i++;
	}
	dest[i] = src[i];
	return (dest);
}

char	*ft_strdup(char *src)
{
	char	*ret;

	ret = malloc(sizeof(src) * (ft_strlen(src) + 1));
	if (!ret)
		return (NULL);
	ret = ft_strcpy(ret, src);
	return (ret);
}
